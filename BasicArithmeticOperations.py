# arithmetic_operations.py
# Examples of performing basic math operations.

a = 10
b = 5

# Addition
sum_result = a + b
print("Sum:", sum_result)

# Subtraction
sub_result = a - b
print("Subtraction:", sub_result)

# Multiplication
mul_result = a * b
print("Multiplication:", mul_result)

# Division
div_result = a / b
print("Division:", div_result)


# Output: Sum: 15
Subtraction: 5
Multiplication: 50
Division: 2.0